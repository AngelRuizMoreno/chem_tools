# Function to create spheres
def create_spheres_from_data(session, sphere_data):
    for sphere in sphere_data:
        center = sphere['center']
        radius = sphere['radius']
        color = sphere['color']
        name = sphere['name']
        command = f"shape sphere center {center[0]},{center[1]},{center[2]} radius {radius} color {color} name {name}"
        run(session, command)

